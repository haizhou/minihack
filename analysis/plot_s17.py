# ---------------------------------------------------------------------------
# Exponential moving average helper
# ---------------------------------------------------------------------------

def ema(values, span=10):
    """Pandas-style EMA (alpha = 2 / (span+1))."""
    alpha = 2.0 / (span + 1)
    out = np.empty_like(values, dtype=float)
    out[0] = values[0]
    for i in range(1, len(values)):
        out[i] = alpha * values[i] + (1 - alpha) * out[i - 1]
    return out

